/*
Operadores Lógicos
&& -> AND -> E -> Todas as expressões precisam ser verdadeiras para retornar true
|| -> OR -> OU -> Todas as expressões precisam ser falsas para retornar true
! -> NOT -> NÃO
*/
const expAnd = true && true && true && true;
console.log(expAnd);

const expOr = true || false;
console.log(expOr);

